/**
 * 卦番号から卦を取得
 */
export function getHexagram(number: number): Hexagram | undefined {
  return all64Hexagrams.find((h) => h.number === number)
}

/**
 * バイナリから卦を取得
 */
export function getHexagramByBinary(binary: string): Hexagram | undefined {
  return all64Hexagrams.find((h) => h.binary === binary)
}

/**
 * すべての卦を取得
 */
export function getAllHexagrams(): Hexagram[] {
  return all64Hexagrams
}

/**
 * ランダムな卦を取得
 */
export function getRandomHexagram(): Hexagram {
  const randomIndex = Math.floor(Math.random() * all64Hexagrams.length)
  return all64Hexagrams[randomIndex]
}
